def seed():
    os.makedirs(os.path.dirname(DB_PATH), exist_ok=True)
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()
    cur.executescript(SCHEMA)
    cur.execute("DELETE FROM price_list_items")  # clear before re-seed

    count = 0
    for discount_rate, rows in DATA.items():
        page = PAGE_MAP.get(discount_rate, 1)
        if discount_rate == "0%":
            title = "CAST CARBON STEEL VALVE PRICE LIST"
        else:
            title = f"CAST CARBON STEEL VALVE PRICE LIST({discount_rate})"

        for size_idx, row in enumerate(rows):
            size_a, size_inch = SIZES[size_idx]
            for product_type, construction, pressure_class, col_idx in COLUMNS:
                price = row[col_idx]
                if price is None:
                    continue
                cur.execute(
                    """INSERT INTO price_list_items
                    (source_page, product_type, construction_type, pressure_class,
                     size_a, size_inch, discount_rate, unit_price, source_table_title)
                    VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)""",
                    (page, product_type, construction, pressure_class,
                     size_a, size_inch, discount_rate, price, title),
                )
                count += 1

    conn.commit()
    conn.close()
    print(f"Seeded {count} rows into {DB_PATH}")


if __name__ == "__main__":
    seed()
