SIZE_TO_INCH = {s[0]: s[1] for s in SIZES}


def seed():
    os.makedirs(os.path.dirname(DB_PATH), exist_ok=True)
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()
    cur.executescript(SCHEMA)
    cur.execute("DELETE FROM price_list_items")  # clear before re-seed

    count = 0
    for discount_rate, info in PRICES.items():
        page = info["page"]
        title = info["title"]
        for product_type, pdata in info["data"].items():
            construction = pdata["construction"]
            for pressure_class in ["10K", "20K"]:
                if pressure_class not in pdata:
                    continue
                for size_a, price in pdata[pressure_class].items():
                    if price is None:
                        continue
                    size_inch = SIZE_TO_INCH.get(size_a, "")
                    cur.execute(
                        """INSERT INTO price_list_items
                        (source_page, product_type, construction_type, pressure_class,
                         size_a, size_inch, discount_rate, unit_price, source_table_title)
                        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)""",
                        (page, product_type, construction, pressure_class,
                         size_a, size_inch, discount_rate, price, title),
                    )
                    count += 1

    conn.commit()
    conn.close()
    print(f"Seeded {count} rows into {DB_PATH}")


if __name__ == "__main__":
    seed()
